import { NextIntlClientProvider } from "next-intl";
import { getLocale, getMessages } from "next-intl/server";
import type { Metadata } from "next";
import NextAuthProvider from "@/lib/context/auth-session-provider";
import Dialogs from "@/app/_components/components-common/dialogs";
import "./globals.css";
import { Toaster } from "react-hot-toast";
import { Suspense } from "react";
import Loader from "./_components/components-common/layout/loader";
import InstallButton from "./_components/InstallButton";
import LocaleSwitcher from "./_components/localSwitcher/LocaleSwitcher";

export const metadata: Metadata = {
  title: "Truereff",
  description: "Generated by create next app",
  manifest: "/manifest.json",
  themeColor: "#000000",
};

export default async function RootLayout({
  children,
}: Readonly<{ children: React.ReactNode }>) {
  try {
    const locale = (await getLocale()) || "en";
    const messages = (await getMessages()) || { locale: "en" };

    return (
      <html lang={locale}>
        <head>
          <title>Truereff</title>
          <link rel="manifest" href="/manifest.json" />
          <meta name="theme-color" content="#000000" />
          <link rel="apple-touch-icon" href="/web-app-manifest-192x192.png" />
        </head>
        <body>
          <Suspense fallback={<Loader />}>
            <NextAuthProvider>
              <NextIntlClientProvider messages={messages}>
                {/* <div className="absolute right-8 top-8">
                  <LocaleSwitcher />
                </div> */}
                {children}
                <Dialogs />
                <Toaster position="top-right" />
              </NextIntlClientProvider>
            </NextAuthProvider>
          </Suspense>
          <InstallButton />
        </body>
      </html>
    );
  } catch (e) {
    return null;
  }
}
